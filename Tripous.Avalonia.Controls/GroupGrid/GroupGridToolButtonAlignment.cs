namespace Avalonia.Controls;

/// <summary>
/// Specifies where a group grid toolbar button is aligned.
/// </summary>
public enum GroupGridToolButtonAlignment
{
    /// <summary>
    /// The button is placed after the left-side toolbar buttons.
    /// </summary>
    Left,
    /// <summary>
    /// The button is placed from the right edge of the toolbar.
    /// </summary>
    Right,
}
